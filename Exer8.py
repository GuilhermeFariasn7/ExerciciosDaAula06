bebidas = input("Escolha uma bebida:\nA - àgua\nB - Refrigerante\nC - café\n:")
if(bebidas == "A")or(bebidas == "a"):
    print("Você escolheu àgua, boa escolha!")
elif(bebidas == "B")or(bebidas == "b"):
    print("Você escolheu refrigerante, boa escolha!")

elif(bebidas == "C")or(bebidas == "c"):
    print("Você escolheu café, boa escolha!")

else:
    print("Erro, letra não identificada...")
